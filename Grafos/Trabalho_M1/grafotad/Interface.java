    private void addVerticeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addVerticeActionPerformed
        // TODO add your handling code here:
        String nome = nomeVertice.getText();
        if(!g.existeVertice(nome))
        {
            Vertice v = new Vertice(nome);
            g.addVertice(nome);
            coordenadasX.add(Integer.valueOf(coordenadaX.getText()));
            coordenadasY.add(Integer.valueOf(coordenadaY.getText()));
            vertice1.addItem(nome);
            vertice2.addItem(nome);
            vertices.addItem(nome);
            //System.out.println(coordenadas.get(0)); 
            
            String s = vertices.toString();
            System.out.println(s);
            vertices.setToolTipText("teste");
        }
        else
        {
            showMessageDialog(null, "Já existe um vértice com este nome neste grafo");
        }
        
        
        
    }//GEN-LAST:event_addVerticeActionPerformed

