    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String v1 = vertice1.getSelectedItem().toString();
        String v2 = vertice2.getSelectedItem().toString();
        if(!v1.equals(v2))
        {
            String nome = nomeAresta.getText();
            float valor = Float.valueOf(valorVertice.getText());
            Vertice i = g.getVertice(v1);
            Vertice f = g.getVertice(v2);
            g.addAresta(i, f, valor, nome);
            arestas.addItem(nome);
        }
        else
        {
            showMessageDialog(null, "Escolha vértices diferentes para criar uma aresta");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

